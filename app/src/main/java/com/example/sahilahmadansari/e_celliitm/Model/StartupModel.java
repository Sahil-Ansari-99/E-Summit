package com.example.sahilahmadansari.e_celliitm.Model;

public class StartupModel {
    String url;
    String name;

    public StartupModel(String url, String name) {
        this.url = url;
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public String getName() {
        return name;
    }
}
